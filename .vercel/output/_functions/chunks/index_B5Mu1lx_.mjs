const prerender = false;
const GET = async ({ request, locals }) => {
	const { emdash, user } = locals;
	const dbErr = requireDb(emdash?.db);
	if (dbErr) return dbErr;
	const denied = requirePerm(user, "schema:read");
	if (denied) return denied;
	try {
		const registry = new SchemaRegistry(emdash.db);
		const collections = await registry.listCollections();
		const collectionsWithFields = await Promise.all(collections.map(async (c) => {
			const fields = await registry.listFields(c.id);
			return {
				...c,
				fields
			};
		}));
		if (new URL(request.url).searchParams.get("format") === "typescript") {
			const types = collectionsWithFields.map((c) => generateTypeScript(c)).join("\n\n");
			return new Response(`// Generated by EmDash CLI
// Do not edit manually - run \`emdash types\` to regenerate

import type { PortableTextBlock } from "emdash";

` + types, {
				status: 200,
				headers: {
					"Content-Type": "text/typescript",
					"Cache-Control": "private, no-store"
				}
			});
		}
		const schemaExport = { collections: collectionsWithFields.map((c) => ({
			slug: c.slug,
			label: c.label,
			labelSingular: c.labelSingular,
			description: c.description,
			icon: c.icon,
			supports: c.supports,
			fields: c.fields.map((f) => ({
				slug: f.slug,
				label: f.label,
				type: f.type,
				required: f.required,
				unique: f.unique,
				defaultValue: f.defaultValue,
				validation: f.validation,
				widget: f.widget,
				options: f.options
			}))
		})) };
		const version = await hashString(JSON.stringify(schemaExport));
		return new Response(JSON.stringify({
			...schemaExport,
			version
		}), {
			status: 200,
			headers: {
				"Content-Type": "application/json",
				"Cache-Control": "private, no-store",
				"X-Schema-Version": version
			}
		});
	} catch (error) {
		return handleError(error, "Schema export failed", "SCHEMA_EXPORT_ERROR");
	}
};

